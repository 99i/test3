	// Worker Slider Functionality
	document.addEventListener('DOMContentLoaded', function() {
		const sliderTrack = document.querySelector('.slider-track');
		if (!sliderTrack) return;

		// Clone slides for infinite loop
		const slides = sliderTrack.querySelectorAll('.slide');
		const slideWidth = slides[0].offsetWidth;
		const gap = 20;
		const totalWidth = (slideWidth + gap) * slides.length;

		// Create seamless loop
		function createInfiniteLoop() {
			slides.forEach(slide => {
				const clone = slide.cloneNode(true);
				sliderTrack.appendChild(clone);
			});
			sliderTrack.style.transform = 'translateX(0)';
		}

		createInfiniteLoop();

		// Handle animation reset
		sliderTrack.addEventListener('animationiteration', () => {
			sliderTrack.style.animation = 'none';
			sliderTrack.style.transform = 'translateX(0)';
			sliderTrack.offsetHeight;
			sliderTrack.style.animation = 'scroll 40s linear infinite';
		});

		// Handle slider pause on hover
		const slider = document.querySelector('.slider');
		if (slider) {
			slider.addEventListener('mouseenter', () => {
				sliderTrack.style.animationPlayState = 'paused';
			});

			slider.addEventListener('mouseleave', () => {
				sliderTrack.style.animationPlayState = 'running';
			});
		}
	});

